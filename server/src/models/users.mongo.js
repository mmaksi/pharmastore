const mongoose = require("mongoose");

const usersSchema = new mongoose.Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
  },
);

// Connects usersSchema with the "launches" collection
module.exports = mongoose.model("User", usersSchema);
